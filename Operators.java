class operator {
    public static void main(String[] args) {
        int i=5;
        i=i+10;
        System.out.println(i>1 && i<20);
        System.out.println(32>>9);
        boolean flag=true;
        System.out.println(!flag);
        System.out.println(i++ + ++i);
        System.out.println(i-- - --i);
    }
}
